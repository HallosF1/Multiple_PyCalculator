def sum(mylist):
    result = 0
    for item in mylist:
        result = result + item
    return result


def sub(mylist):
    result = 2
    for item in mylist:
        result = result - item
    return result


def multiply(mylist):
    result = 1
    for item in mylist:
        result = result * item
    return result


def divide(mylist):
    result = 1
    for item in mylist:
        result = item / result
    return result
